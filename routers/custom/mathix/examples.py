# Example 1: for loop
for i in range(5):
    print(i)

# Example 2: while loop
num = 0
while num < 5:
    print(num)
    num += 1

# Example 3: if-elif-else statements
x = 10
if x > 10:
    print("x is greater than 10")
elif x == 10:
    print("x is equal to 10")
else:
    print("x is less than 10")
